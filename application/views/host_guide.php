        	<h4>Expectations And Responsibilities</h4>
            <hr />
            <ul style="list-style:none;">
                <li>1. The Host Friend Program is NOT a home-stay program.</li>
                <br />
                <li>2. Students are assigned to a Friendship Partner for a period of one year,  although the relationship may well continue beyond that year.</li>
                <br />
                <li>3. We suggest that you consider "adopting" at least 2 students at one time.  Many students come to the welcome reception with a roommate 
                or friend.  Having someone familiar with them as they visit with you is comforting, for both the student and the host friend.</li>
                <br />
                <li>4. If for any reason whatsoever the student and host are not compatible, it is reasonable and possible to request a change.  Do not 
                remain unhappy about the situation or simply neglect to contact the student.  Instead, call the coordinators, Jon or Alana Varner 
                (864-723-0821) and discuss your problem.</li>
                <br />
                <li>5. You are asked to contact your student at least once a month.  It may be just a friendly call, encouraging email, or an invitation.  
                Keep in contact to show your interest and let your student know you are available.</li>
                    <ul><br />
                        <li> If you are away for holidays or on vacation, let your student know.  A lack of contact can cause misunderstandings.</li>
                        <br />
                        <li> When arranging a meeting, be sure the student knows when and where it will be and what time you will be returning them to 
                        their apartment.  Transportation should be provided for the first few meetings, even if the student has their own car.</li>
                    </ul>
                <br />
                <li>6. It is helpful to be as familiar as possible with the cultural background of the student, the geography of his/her country and a few 
                facts about it.</li>
                <br />
                <li>7. Religion and religious institutions are important to American culture.  You are free to invite students to attend worship services 
                with you, particularly during religious holidays, but you should indicate clearly the nature of the event as well as its sponsorship and let 
                the student decide if they will participate.  Many students appreciate the opportunity to attend church events as a part of their cultural 
                experience, but it is the policy of CAIF that proselytizing is not acceptable.</li>
 				<br />
  				<li>8. Some students have dietary restrictions based on religious or cultural preferences.  Refer to your copy of the student's application.
                See "What to Serve--Meal Tips" in the link above.</li>
 				<br />
  				<li>9. We suggest that, if possible, you invite the students for Thanksgiving dinner as well as some activity during the Christmas season and
                semester breaks.  These may be lonely periods for them and it will give them good insights into our culture.<br />
        		&nbsp;&nbsp;&nbsp;&nbsp; We encourage you to make your students a part of the family's informal living rather than treating them like a 
                guest.  Let them see you in everyday living situations and let them help you with tasks and activities as appropriate.</li>
 				<br />
  				<li>10. Encourage your student to explain his/her customs, country, food, etc.  It may be that the student will want to prepare some food for
                you in your kitchen.</li>
 				<br />
  				<li>11. Some internationals are uncomfortable with housepets.  You should ask ahead of extending your first invitation and keep pets locked 
                up if your guests are allergic or fearful.</li>
 				<br />
  				<li>12. Do not assume responsibility for financial, immigration, academic, employment, health or serious adjustment problems.   Refer these 
                problems to Andy Kabasele in the Office of Global Engagement (656-0490) or contact CAIF President, Kathy Mabry at 
                882-8141.</li>
        	</ul>            
