        <div class="well span4">
        	<h4>Suggestions For First Meeting</h4>
            <hr />
            &nbsp;&nbsp;&nbsp;&nbsp; When you receive the name of your student, get in touch as soon as possible by phone or email to arrange your first 
            meeting.  Early contact is important since the first few weeks are often the most difficult for a student.  Please identify yourself as part of 
            the CAIF International Friends Program.
            <br /><br />
            &nbsp;&nbsp;&nbsp;&nbsp; The student will have received your name and number.  However, a written note (or email) with your name, address, phone 
            number, email address, time and place of the first meeting, length of the visit and appropriate dress can prevent misunderstandings.  Even if the
            student has transportation, it is best to provide it the first time.
            <br /><br />
            &nbsp;&nbsp;&nbsp;&nbsp; Learn to pronounce the student's name correctly and use the name he/she prefers.  Refrain from assigning an English name
            unless your student suggests it.
            <br /><br />
            &nbsp;&nbsp;&nbsp;&nbsp; Remember:  the student's first priority is to study.  These young people are very serious students and keep hectic class
            and research schedules. You may find that you want to see him/her more often than he/she can spare the time.  Do not be discouraged if this 
            happens and do not consider this a rejection of your time and interest.  Continue to extend invitations, even if only for a brief coffee break.  
            Sometimes a well-timed email or phone call of encouragement goes further than a lengthy visit.
