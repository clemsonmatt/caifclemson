        	<h4>Jump-Start Your Conversations</h4>
            <hr />
            <ul>
                <li>Geographical Questions
                	<ul>
                	<li>Where is your city?</li>
                	<li>What would you like to see while you are in America?</li>
                	</ul>
                </li>
                <li>Photographs/Family
                	<ul>
                    <li>Do you have family photographs that I could see?</li>
                    <li>How large is your family?</li>
                    <li>What is your extended family like?</li>
                	</ul>
                </li>
                <li>Languages
                	<ul>
                    <li>What languages do you speak?</li>
                    <li>Ask how to say various greetings & other common phrases in his/her language</li>
                    <li>Offer to help with any expressions or concepts in English with which he or she is having difficulty.</li>
               		</ul>
                </li>
                <li>Money</li>
                <li>Socializing, Dating & Marriage
                	<ul>
                    <li>In your culture, how do young men and women get to know each other?  Are marriages arranged?</li>
                    <li>What are some of the wedding customs?</li>
                	</ul>
                </li>
                <li>Family Life
                	<ul>
                    <li>What are your important holidays?</li>
                    <li>How are children disciplined?</li>
                    <li>What is the average number of children in a family?</li>
               		</ul>
                </li>
               	<li>Men's and Women's Roles</li>
               	<li>Recreation
                	<ul>
                    <li>What games/sports are popular in your country?</li>
                	</ul>
                </li>
                <li>Education
                	<ul>
                    <li>What are some differences between your educational sytem and ours?</li>
                    <li>What degree are you pursuing?</li>
                    <li>NOTE:  if you are a homeschooling family, be aware that this is a very new and different concept to most foreigners.</li>
                	</ul>
                </li>
                <li>Transportation</li>
                <li>Food
                	<ul>
                    <li>What was your first reaction to American food?</li>
                   	<li>Are your foods highly seasoned?</li>
                    <li>What is a typical breakfast like for you?</li>
                	</ul>
                </li>
                <li>Spiritual Topics
                	<ul>
                    <li>Please remember that proselytizing is not acceptable within CAIF, but most internationals will freely discuss spiritual topics.  Be respectful as opinions vary widely across cultures.</li>
                    <li>I'd like to know more about your religious beliefs.</li>
                    <li>What is your perception of religion in this country?</li>
                    <li>What do you think is the meaning of life?</li>
        			</ul>
                </li>
            </ul>
