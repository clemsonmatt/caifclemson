        <div class="well span6">
            <h4>Some Basics</h4>
            <hr />
            <ul>
                <li>Listen attentively</li> 
                <li>Speak carefully to be understood
                    <ul>
                        <li>Remember to articulate well, speak slowly and clearly.  (But not LOUDER--they're not deaf!)</li>
                    </ul>
                </li>
                <li>Avoid using idioms and slang in conversation. 
                    <ul>
                        <li>These terms can be fun to discuss as a conversational topic, but make it clear to the student that it is a teaching time.</li>
                    </ul>
                </li>  
                <li>Use jokes and humor sparingly.  Often the joke gets lost because of lack of cultural context.</li> 
                <li>Explain words and phrases patiently.</li>
                <li>Respect differences of opinion.</li>
            </ul>
        </div>
        <div class="well span6">
        	<h4>Non-verbal Communications</h4>
            <hr />
            <ul>
            	<li>Be aware of body language</li>
                <li>Touch - be careful and sensitive in this area.  Some cultures are very "touchy" with greetings (kisses, hugs), while others almost never touch.</li>
                <li>Personal Space - the amound needed varies drastically from culture to culture.</li>
                <li>Formality & Informality - Americans tend toward a more informal lifestyle.  In other cultures, formality in dress, in language, and in greeting another shows respect for the host or declares one's social status.</li>
                <li>Time Perception - Western culture runs by the clock, but other cultures are not as "time conscious".</li>
            </ul>
