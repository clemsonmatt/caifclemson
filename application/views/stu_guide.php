        	<h4>Guidelines For International Students</h4>
            <hr />
        	<ul style="list-style:none;">
            	<li>1. As you may know, most Americans carefully observe the time that is set for people to meet.  Please try to be on time and be sure to 
                call if you have to change the time or have to cancel the appointment.</li>
                <br />
    			<li>2. If you would like to bring friends along with you when you get together with your friendship partner, you need to ask permission in 
                advance.</li>
                <br />
    			<li>3. You will be invited to join in activities when you have the time.  On the average, a student will meet with their friendship partner 
                about every 4-6 weeks.</li>
                <br />
    			<li>4. Notify your friendship partner, as well as your foreign student advisor, if you change your address, email, or telephone.  If you have
                not heard from your host for more than two months, please notify CAIF President, Kathy Mabry at 882-8141 or at sccaif@gmail.com</li>
                <br />
    			<li>5. Occasionally, you may wish to invite your friendship partner to attend a university function, or you may want to prepare one of your 	
                national food dishes for him or her and friends of the family.  Please feel free to extend an invitation.</li>
                <li>6.  You will probably be invited to participate in special holiday activities with your host.  Feel free to ask about the particular 
                celebration, why and how it is observed, and other questions you might have.</li>
                <br />
				<li>7.  You will receive information about upcoming activities for all members of the organization, including students and friendship 
                partners.  You are welcome to attend all activities even though your friendship partner may not be able to participate.</li>
                <br />
				<li>8.  Your host friend is assigned to you for a period of one year.  However, the relationship may continue beyond a year.</li>
                <br />
				<li>9. If you should for any reason feel uncomfortable with the host friend assigned to you, please notify host friend coodinators, Jon or 
                Alana Varner, right away.</li>
                <br /> 
				<li>10.  Host friends are involved in CAIF because they are truly interested in getting to know international students and they want to be 
                your friends.  Please do not hesitate to ask them any questions you have about American culture, family life, or any social issues that you 
                do not understand.</li>
