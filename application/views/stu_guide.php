        <div class="well span6">
        	<h4>Student-Host Relationship Tips</h4>
            <hr />
            <ul style="list-style:none;">
        		<li>1. Americans are usually very "friendly" and will readily welcome and help newcomers.  However, "friendship" takes a long time to develop.</li>
                <br />
    			<li>2. Americans often indicate their interest in others by asking questions.  This indicates an eagerness to learn about you and your culture.</li>
                <br />
    			<li>3. You are expected to be ready at the time agreed upon if someone is picking you up at your residence.  Many Americans feel that arriving 
                late for appointments or events indicates rudeness or lact of respect when keeping someone waiting.</li>
    			<br />
                <li>4. Everyday casual dress is appropriate for most visits to peoples' homes.  Your host should indicate ahead of time if more formal attire is
                required for a particular event.</li>
                <br />
    			<li>5. It is proper to shake hands with everyone to whom you are introduced, both men and women.  Some Americans, especially if well-acquainted,
                will greet each other with a hug.  If you do not wish to be hugged, extend your hand quickly for a handshake.</li>
                <br />
    			<li>6. When you accept a meal invitation, tell your host if you have any dietary restrictions or allergies.  He/she will appreciate this 
                information.</li>
                <br />
    			<li>7. It is not necessary to take a gift when you are invited to dinner.</li> 
    			<br />
                <li>8. If you are a smoker, ask your host's permission before smoking.  Most non-smoking Americans do not allow smoking in their home.</li>
    			<br />
                <li>9. Many Americans have pets; especially dogs and cats that are often allowed into every room in their homes.  If the presence of pets makes 
                you uncomfortable, discuss the situation with your host.</li>
                <br />
    			<li>10. You may offer to assist with household tasks (like meal preparation, clearing the table, etc) as a gesture of appreciation.</li>
        	</ul>
